public class Cliente {
    String nome;
    String telefone;

    public Cliente() {
    }

    //Getter do nome
    public String getNome() {
        return nome;
    }

    //Setter do nome
    public void setNome(String nome) {
        this.nome = nome;
    }

    //Getter do telefone
    public String getTelefone() {
        return telefone;
    }

    //Setter do telefone
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

}